def labels_init(batch_size, lab, err):
    err_labels = int(batch_size*err)
    if lab==1:
        x = torch.cat((torch.ones((batch_size-err_labels,)), torch.zeros((err_labels,))), 0)
    else:
        x = torch.cat((torch.zeros((batch_size-err_labels,)), torch.ones((err_labels,))), 0)
    y = x[torch.randperm(batch_size)].to(device)
    return y